package ex_03_Java_Operators;

public class Lab_018_Operators_Relational {

    public static void main(String[] args) {
        // < less than
        // <= less tahn or equal to
        // > Greater
        // >= Greater or equal
        // == Equal to
        // != not equal
        // All of the above examples will result in boolean output

        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int mamitha_age = 32;
        int pramod_age = 34;
        boolean result = pramod_age >= mamitha_age;
        System.out.println(result);

    }
}
