package ex_03_Java_Operators;

public class Lab_017_Operators_Unary {

    public static void main(String[] args) {

        //Unary operator
        int a = +10;
        System.out.println(a);

        int a1 = -110;
        int result = a+a1;
        System.out.println(a);
        System.out.println(a1);
        System.out.println(result);

        int b = -1;
        b = b+1;
        System.out.println(b);

    }
}
