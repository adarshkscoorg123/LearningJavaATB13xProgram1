package ex_08_Java_IncrementDecrement;

public class Lab_50_Dec {

    public static void main(String[] args) {
        int a = 10;   // a = 10
        int result_post = a--;   // a--  -> 10   a = 9
        System.out.println(result_post); // result_post = 10
        System.out.println(a); // a = 9
    }
}
