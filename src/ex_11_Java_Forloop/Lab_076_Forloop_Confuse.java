package ex_11_Java_Forloop;

public class Lab_076_Forloop_Confuse {

    public static void main(String[] args) {

        for ( int i = 0; i < 10; i++){ // 0 to 9 , 10 times
            System.out.println(i);
        }
        System.out.println("------");

        for ( int j = 1; j <= 10; j++){ // 1 to 10, 10 times
            System.out.println(j);
        }
    }
}
